package basic_operators;

public class Logical_operators_class 
{
	public static void main(String[] args) {
		
		boolean  a = true ;
		boolean  b = false ;
		
		System.out.println("a && b=" + (a && b) );
		
		System.out.println("a || b=" + (a || b));
		
		System.out.println("!(a&&b)=" + !(a && b));
	}
	

}
